'''
Arithmetic Opeartors in Python:-
    - + (Addition)
    - - (Substraction)
    - * (Multiplication)
    - / (Division)
    - // (Floor Division)
    - % (Modulo (Return Remainder))
    - ** (Exponential Operator)
'''

print(5 + 4)
print(5 - 4)
print(5 * 4)
print(5 / 4)
print(5 // 4)
print(5 % 4)
print(5 ** 4)